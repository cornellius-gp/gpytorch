class TestVoronoiIndex(TestBaseIndex, unittest.TestCase):
    test_names = test_names
    test_inputs = test_inputs
    expected_outputs = expected_outputs
    new_orders = new_orders

    def test_set_blocks(self):
        for test_name, test_input, expected in zip(self.test_names, self.test_inputs, self.expected_outputs):
            with self.subTest(test_name):
                # ensure inducing points and voronoi regions are what they should be
                # approximate equality accounts for having copied these results from a notebook
                self.assertTrue(torch.allclose(test_input.inducing_points, expected["inducing_points"], rtol=1e-3))
                self.assertTrue(
                    all(
                        torch.equal(actual_region, expected_region)
                        for actual_region, expected_region in zip(test_input.regions, expected["regions"])
                    )
                )

                # ensure each block is what it should be
                self.assertTrue(
                    all(
                        torch.equal(actual_block, expected_block)
                        for actual_block, expected_block in zip(test_input.blocks, expected["blocks"])
                    )
                )

    def test_set_neighbors(self):
        for test_name, test_input, expected in zip(self.test_names, self.test_inputs, self.expected_outputs):
            with self.subTest(test_name):
                # ensure each neighbor block is what it should be
                # sorted because neighbor blocks are not expected to be ordered within blocks
                self.assertTrue(
                    all(
                        torch.equal(actual_neighbors.sort()[0], expected_neighbors.sort()[0])
                        for actual_neighbors, expected_neighbors in zip(test_input.neighbors, expected["neighbors"])
                    )
                )

    def test_set_test_blocks(self):
        for test_name, test_input, expected in zip(self.test_names, self.test_inputs, self.expected_outputs):
            with self.subTest(test_name):
                # get block membership of new data points
                test_input.block_new_data(test_data)

                # ensure each block is what it should be
                self.assertTrue(
                    all(
                        torch.equal(actual_block, expected_block)
                        for actual_block, expected_block in zip(test_input.test_blocks, expected["test_blocks"])
                    )
                )

    def test_reorder(self):
        """
        This only tests that the order-dependent quantities defined in the child class are reordered properly,
        as all other order-dependent quantities are defined by the parent and therefore tested in the parent test class.
        """

        def new_ordering_strategy(order):
            return lambda x: order

        for test_name, test_input, expected, new_order in zip(
            self.test_names, self.test_inputs, self.expected_outputs, self.new_orders
        ):
            with self.subTest(test_name):

                # reorder blocks
                test_input.reorder(new_ordering_strategy(new_order[0]))
                # ensure new inducing points are ordered correctly, all other ordered quantities checked in parent test
                self.assertTrue(
                    torch.allclose(test_input.inducing_points, expected["inducing_points"][new_order[0]], rtol=1e-3)
                )
                # correctness of regions after reordering
                self.assertTrue(
                    all(
                        torch.equal(actual_region, expected_region)
                        for actual_region, expected_region in zip(
                            test_input.regions, [expected["regions"][idx] for idx in new_order[0]]
                        )
                    )
                )

                # new order
                test_input.reorder(new_ordering_strategy(new_order[1]))
                # ensure new inducing points are ordered correctly
                self.assertTrue(
                    torch.allclose(
                        test_input.inducing_points, expected["inducing_points"][new_order[0]][new_order[1]], rtol=1e-3
                    )
                )
                # correctness of regions after next reordering
                self.assertTrue(
                    all(
                        torch.equal(actual_region, expected_region)
                        for actual_region, expected_region in zip(
                            test_input.regions,
                            [[expected["regions"][idx] for idx in new_order[0]][idx2] for idx2 in new_order[1]],
                        )
                    )
                )

                # finally, create an ordering based on composing previous two to get us back to where we started
                final_order = torch.argsort(torch.stack([new_order[0][i] for i in new_order[1]]))
                # reorder blocks
                test_input.reorder(new_ordering_strategy(final_order))
                # ensure new inducing points are ordered correctly
                self.assertTrue(torch.allclose(test_input.inducing_points, expected["inducing_points"], rtol=1e-3))
                # correctness of regions after final reordering
                self.assertTrue(
                    all(
                        torch.equal(actual_region, expected_region)
                        for actual_region, expected_region in zip(test_input.regions, expected["regions"])
                    )
                )


